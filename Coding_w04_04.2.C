#include <stdio.h>

int main() {
    float score = 90.5;
    printf("Score = %.1f\n", score);
    return 0;
}
// 1.ใช้ int: Score=90, ใช้ fioat: Score=90.5 (ใช้intไม่เเสดงเลขทศนิยม)
// 2.ทําไมต้องเลือก data type ให้ถูกต้อง? = เพื่อให้ข้อมูลออกมาตามที่ต้องการ ไม่ตกหล่นหรือผิดพลาด
// 3.อภิปรายเเละสรุปผลที่ได้จากการทดลอง = การเลือกใช้ data type ไม่ถูกต้องจะทําให้ค่าไม่ถูกต้องหรือเป็นไปตามที่ต้องการ